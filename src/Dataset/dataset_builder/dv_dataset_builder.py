"""
–ú–æ–¥—É–ª—å –¥–ª—è –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –¥–∞—Ç–∞—Å–µ—Ç–∞ –∏–∑ JSON-—Ñ–∞–π–ª–∞ —ç–∫—Å–ø–æ—Ä—Ç–∞ —á–∞—Ç–∞ Telegram.

–≠—Ç–æ—Ç –º–æ–¥—É–ª—å –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è–µ—Ç –∫–ª–∞—Å—Å DatasetBuilder –¥–ª—è –ø–∞—Ä—Å–∏–Ω–≥–∞ JSON-—Ñ–∞–π–ª–∞
—ç–∫—Å–ø–æ—Ä—Ç–∞ —á–∞—Ç–∞ —Å –±–æ—Ç–æ–º "–î–∞–π–≤–∏–Ω—á–∏–∫" –∏ —Å–æ–∑–¥–∞–Ω–∏—è –¥–∞—Ç–∞—Å–µ—Ç–∞ —Å –º–µ—Ç–∫–∞–º–∏ –ª–∞–π–∫–æ–≤/–¥–∏–∑–ª–∞–π–∫–æ–≤.
"""

import json
import pandas as pd


class DatasetBuilder:
    """
    –ö–ª–∞—Å—Å –¥–ª—è –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –¥–∞—Ç–∞—Å–µ—Ç–∞ –∏–∑ JSON-—Ñ–∞–π–ª–∞ —ç–∫—Å–ø–æ—Ä—Ç–∞ —á–∞—Ç–∞ —Å –±–æ—Ç–æ–º "–î–∞–π–≤–∏–Ω—á–∏–∫".

    –ö–ª–∞—Å—Å –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ JSON-—Ñ–∞–π–ª–∞, –∏–∑–≤–ª–µ–∫–∞–µ—Ç —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏ –ø—Ä–æ—Ñ–∏–ª–µ–π
    –∏ –º–µ—Ç–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏—Ö —Ä–µ–∞–∫—Ü–∏–π (–ª–∞–π–∫/–¥–∏–∑–ª–∞–π–∫), —Ñ–æ—Ä–º–∏—Ä—É—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –¥–∞—Ç–∞—Å–µ—Ç.
    """

    def __init__(self, path_to_json):
        """
        –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç DatasetBuilder —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º JSON-—Ñ–∞–π–ª–æ–º.

        Args:
            path_to_json (str): –ü—É—Ç—å –∫ JSON-—Ñ–∞–π–ª—É —ç–∫—Å–ø–æ—Ä—Ç–∞ —á–∞—Ç–∞
        """
        with open(path_to_json, "r", encoding="utf-8") as f:
            self.data = json.load(f)

        # –ò–º—è –±–æ—Ç–∞, –∫–æ—Ç–æ—Ä–æ–µ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø—Ä–æ—Ñ–∏–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
        self.bot_name = "–î–∞–π–≤–∏–Ω—á–∏–∫ | Leo ‚Äì –∑–Ω–∞–∫–æ–º—Å—Ç–≤–∞, –æ–±—â–µ–Ω–∏–µ –∏ –Ω–æ–≤—ã–µ –¥—Ä—É–∑—å—è"

        # –ò—Å–∫–ª—é—á–µ–Ω–∏—è - —Ç–µ–∫—Å—Ç–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ –¥–æ–ª–∂–Ω—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å—Å—è –∫–∞–∫ —Ä–µ–∞–∫—Ü–∏–∏
        self.parse_exception = {"üöÄ –°–º–æ—Ç—Ä–µ—Ç—å –∞–Ω–∫–µ—Ç—ã", "–ù–µ—Ç", "1 üöÄ", "1 üëç"}

    def build_dataset(self):
        """
        –°—Ç—Ä–æ–∏—Ç –¥–∞—Ç–∞—Å–µ—Ç –∏–∑ —Å–æ–æ–±—â–µ–Ω–∏–π —á–∞—Ç–∞.

        –ú–µ—Ç–æ–¥ –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ JSON-—Ñ–∞–π–ª–∞ –∏ —Å–æ–∑–¥–∞–µ—Ç –¥–∞—Ç–∞—Ñ—Ä–µ–π–º pandas
        —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –ø—Ä–æ—Ñ–∏–ª—è—Ö –∏ —Ä–µ–∞–∫—Ü–∏—è—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è. –ë–µ—Ä—É—Ç—Å—è —Ç–æ–ª—å–∫–æ —Å–æ–æ–±—â–µ–Ω–∏—è
        –æ—Ç –±–æ—Ç–∞ (—Å —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏—è–º–∏ –ø—Ä–æ—Ñ–∏–ª–µ–π) –∏ –æ—Ç–≤–µ—Ç—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–ª–∞–π–∫–∏/–¥–∏–∑–ª–∞–π–∫–∏).

        Returns:
            pd.DataFrame: –î–∞—Ç–∞—Ñ—Ä–µ–π–º —Å –∫–æ–ª–æ–Ω–∫–∞–º–∏ ["profile_id", "image_path", "image_index", "profile_liked"],
                          –≥–¥–µ profile_liked –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –∑–Ω–∞—á–µ–Ω–∏—è 0 (–¥–∏–∑–ª–∞–π–∫) –∏–ª–∏ 1 (–ª–∞–π–∫)
        """
        rows = []

        current_profile_photos = []  # –°–ø–∏—Å–æ–∫ —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–π —Ç–µ–∫—É—â–µ–≥–æ –ø—Ä–æ—Ñ–∏–ª—è
        profile_id = 0  # ID –ø—Ä–æ—Ñ–∏–ª—è

        for msg in self.data["messages"]:
            # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏–π –æ—Ç –±–æ—Ç–∞ (—Å–æ–¥–µ—Ä–∂–∞—â–∏—Ö —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏ –ø—Ä–æ—Ñ–∏–ª–µ–π)
            if msg.get("from") == self.bot_name:
                if "photo" in msg:
                    current_profile_photos.append(msg["photo"])
                elif "file" in msg:
                    current_profile_photos.append(msg["file"])

            # –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—Ç–≤–µ—Ç–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (—Ä–µ–∞–∫—Ü–∏–∏ –Ω–∞ –ø—Ä–æ—Ñ–∏–ª–∏)
            else:
                text = msg.get("text")

                # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –∏—Å–∫–ª—é—á–µ–Ω–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è
                if text in self.parse_exception:
                    continue

                # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ –ª–∞–π–∫–∏ –∏ –¥–∏–∑–ª–∞–π–∫–∏
                if text not in {"‚ù§Ô∏è", "üëé"}:
                    continue

                # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –º–µ—Ç–∫—É: 1 –¥–ª—è –ª–∞–π–∫–∞, 0 –¥–ª—è –¥–∏–∑–ª–∞–π–∫–∞
                profile_liked = 1 if text == "‚ù§Ô∏è" else 0
                final_label = profile_liked

                # –î–æ–±–∞–≤–ª—è–µ–º –∫–∞–∂–¥—É—é —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏—é –ø—Ä–æ—Ñ–∏–ª—è –≤ –¥–∞—Ç–∞—Å–µ—Ç
                for idx, photo_path in enumerate(current_profile_photos):
                    rows.append({
                        "profile_id": profile_id,
                        "image_path": photo_path,
                        "image_index": idx,
                        "profile_liked": final_label
                    })

                profile_id += 1  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É –ø—Ä–æ—Ñ–∏–ª—é
                current_profile_photos = []  # –û—á–∏—â–∞–µ–º —Å–ø–∏—Å–æ–∫ —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–π

        return pd.DataFrame(
            rows,
            columns=["profile_id", "image_path", "image_index", "profile_liked"]
        )

    def export_to_csv(self, output_path="files/processed/dv_dataset_raw.csv"):
        """
        –≠–∫—Å–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç –ø–æ—Å—Ç—Ä–æ–µ–Ω–Ω—ã–π –¥–∞—Ç–∞—Å–µ—Ç –≤ CSV-—Ñ–∞–π–ª.

        –ú–µ—Ç–æ–¥ –≤—ã–∑—ã–≤–∞–µ—Ç build_dataset() –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ —É–∫–∞–∑–∞–Ω–Ω—ã–π CSV-—Ñ–∞–π–ª.

        Args:
            output_path (str): –ü—É—Ç—å –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è CSV-—Ñ–∞–π–ª–∞ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é "files/processed/dv_dataset_raw.csv")
        """
        df = self.build_dataset()
        df.to_csv(output_path, index=False, encoding="utf-8")
